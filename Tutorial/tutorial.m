:S3 輪郭線を描く

まず最初に、念のためカーソルが作業面から離れていないことを確認してください。$
よくわからない場合は、作業面上で右クリックメニューから「カーソル位置をここに移動」を実行するか、 %S21%の方法で一旦別の作業面に設定してから、もう一度作業面を設定しなおしてください。$

|S31 大まかな輪郭線を描く

輪郭線を描くには以下の２通りの方法があります。$
$
1.%PolylineTool%モードにして、こつこつ描く。$
マウスを左クリックする度に、一つ前にクリックした点との間に線を引きます。
線を引くのを一旦停止したい場合は、%BreakPop%を実行してください。$
$
2.%FreehandlineTool%モードで一気に描く。タブレットユーザーならばこちらの方が
手早くできます。$
マウスの左ボタンを押してドラッグすると、カーソルの移動軌跡に沿った形でラインが
引かれます。$
$
下図は%FreehandlineTool%を使って描いた輪郭線です。$

@#tut_elepline.gif@
$
|S32 輪郭線の形を整える
輪郭線が思い通り描ければ、この部分は飛ばして次の項目に移っても構いません。$

$
次の方法で%MoveTool%ツールで頂点位置を微調整します。$
$
1.ツールボタンで%MoveTool%モードを選択する$
2.%BreakPop%を数回実行して%MoveTool%モードにする$
$
(Esc)を連打するだけでよいので、2の方法をお勧めします。$
$
$
