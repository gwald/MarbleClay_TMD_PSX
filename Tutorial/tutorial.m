@Header
:Detail 細部を加工する
これまでの作業でおおまかに形はできあがりましたが、更にディテールを追加したい場合もあると思います。$
このセクションではディテールを作りこむために使えるいくつかの機能を紹介します。$
$
|D1 セグメントラインを追加する
%AddPointTool%ツールを使って面を連続的に分割することができます。$
分割によってできるエッジをセグメントラインと呼びます。$
$
1. %AddPointTool%ツールを選択し、分割したいエッジ上を、Shiftキーを押しながら左クリック
@#tut_detail1.gif@$
$
2. そのエッジを構成する面につながった面が連続的に分割されます
@#tut_detail2.gif@$
$
|D2 セグメントラインを消去する
追加したセグメントラインは次の方法を使って消去することができます。$
$
1. %MoveTool%ツールを選択し(%BreakPop%キーを押す)、セグメントラインをダブルクリックして、セグメントラインを選択
@#tut_detail3.gif@$
$
2. %EditDeleteEx%を一回実行するとラインが半確定エッジになります。$
$
3. さらにもう一度%EditDeleteEx%を実行すると、ラインが消え、ラインを挟む面が統合されます。$   
@#tut_detail4.gif@$
$
|D3 マウス操作による頂点溶接
二つの頂点を溶接する場合、%FacetTool%を使ってマウス操作による溶接が可能です。$
(三個以上の頂点を溶接する場合は、%MeshMenu%->%MeshWeld%を使います。)$
$
1.%FacetTool%を選択し、溶接したい一方の頂点をドラッグします。$
$
2.ドラッグを開始すると、頂点からカーソルの位置まで矢印が表示されます。$
このまま、もう一方の頂点までドラッグし、もう一方の頂点がハイライト表示されたらマウスボタンを離してください。$

@#tut_dragnweld1.gif@$
$
3. 二つの頂点が溶接されます。. $
@#tut_dragnweld2.gif@$

|D4 その他の方法
これ以外にも%FacetTool%ツールを用いた四角面の三角面分割等、色々な編集手段があります。$
$
詳しくはヘルプ"%PolygonOperation%"を参照してください。$
$

                  
