@Header

|S36 輪郭線を確定する

輪郭線をそのまま利用してモデリングを行っても良いのですが、 フリーハンド折れ線 等で描いた輪郭線は、頂点数が多くなりがちなので輪郭線をロックしてモデリングのガイドとして利用することをお勧めします。$
$
1. 手順どおり輪郭線を描いた場合、 輪郭線は"NewObject"の"Mesh-0"という名前でオブジェクトウインドウに登録されているはずです。 オブジェクトウインドウを開き、NewObjectの名前を"Eleph"、 Mesh-0の名前を"outline"に便宜上変更しておきます。(①で入力後エンターキー)
"outline"行の右端の青い南京錠のアイコン②をクリックし南京錠を閉じると"outline"がロックされます。$
$
@#tut_lockoutline.gif@
$
ロックされた輪郭線は頂点が表示されず、スナップは効きますが削除や移動が出来ないようになります。$
$
<I>頂点を表示したい場合は[%ViewMenu%]->[ロックされた頂点を表示する]にチェックをいれます。</I>$
$
2. "Eleph"が選択状態になっていない場合は、①の部分をクリックして選択状態にしておいてください。$
(この部分が選択されていないと、以降の操作で正しく面を生成できません)$
@#tut_selectobj.gif@$

$
