:Bend 曲げ加工

象にポーズをとらせるため、手を加えます。$
鼻や、足など細長い部分を加工するのには、%MoveTool%ツールの%SelectMagnet%モードの
機能である「スムースベンド」を使うと便利です。$

$
1. 選択モードから%SelectMagnet%を選んでください。 選択モードを変更すると自動的に
%MoveTool%ツールになります。$
マグネット選択の影響範囲がカーソルの周りに円状に表示されますので、
鼻を８割ほど選択するくらいの大きさに設定してください。$
影響範囲はマウスの右ボタンを押しながら右にドラッグすると大きくなり、
左にドラッグすると小さくなります。$
$
@#tut_nosebend1.gif@$
$
2. 鼻の先端の頂点を選択し、ドラッグして移動させます。$
ドラッグ中に[Ctrl]キーを押すと、曲げ変形を加えることができます。$
(オプションでスムースベンドの「自動」にチェックを入れておくと、Ctrlキーを
押さなくても曲げ変形を行うことができるようになります)$
$
@#tut_nosebend2.gif@
$
$
3. 鼻以外の部分(足等)も同様にして曲げます。$
足などは左右非対称に変形させたいので、%MirrorFreezMirror%を行い、対称図形を
固定・解除した後で加工してください。$

