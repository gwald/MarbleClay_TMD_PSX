:MakeElep 象を作る
 出来るだけ実用に即した形で紹介するため、いきあたりばったりで
オブジェクトを作ります。 とりあえず形に特徴のある象を作ってみることに
します。象なら鼻と耳を抑えておけば、少々形が崩れても大丈夫でしょう。$
$
 第2版ですがまだ文体が統一できてません。 おかしい所、わかり難い所があれば指摘してください。$
$

:S2 準備
|S21 作業面の設定
$
#<IMG SRC="../HelpImg/tut_workplane.jpg" align="LEFT" >
$
①を押すと、作業画面が４面図形式に切り替わります。$
３面図上でのモデリング作業も可能ですが、以降は%lay_PersWay%を中心に説明します。$
$
③～⑧のどれかをクリックし、作業面を選択します。$
  ③ XY平面 ④YZ平面 ⑤XZ平面 ⑥スクリーンに平行な面 ⑦選択ポリゴンに平行な面を作業面に設定します。$
作業面を設定すると、以降の操作は、全て作業面を基準に平行な面に対して行われます。$
$
ここでは、⑤XZ平面を選択しておいてください。$

|S22 視点の決定
$
モデリングしやすいように、視点を変更します。$

%QuickArea%上にカーソルを移動すると、カーソル形状が変化します。$
デフォルトの状態で%lay_PersWay%で操作している場合、カーソルは視点変更モード
の形状になります。この状態でマウスの左ボタンを押しながらドラッグするとカメラの
位置が注視点を中心に回転します。$
マウスの右ボタンを押しながら縦方向にドラッグすると、カメラがズームします。$
$
SPACEキーとマウスの左ボタンを押しながらドラッグすると、注視点が移動します。$
$
視点の変更は、%QuickArea%での方法以外にも、%ViewTools%で視点移動モードにするか、
Alt＋ドラッグ、中ボタン＋ドラッグ等でも行うことができ、 これらはユーザーの好みに合わせてカスタマイズすることが出来ます。$
$
ここでは、XZ平面を横から眺めるような位置に変更してください。$
$
|S23 確認
$
XZ平面が作業面で、 視点はXZ平面を横から眺める位置になっていることを確認してください。$
$
@#tut_xzview.gif@
$$$

