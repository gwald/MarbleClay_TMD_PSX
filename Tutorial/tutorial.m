:MirrorEdit 対称図形を作成

MarbleCLAYの現行バージョンでは、対称図形の作成に以下のような制限・特徴があります。$
i)面ごとに対称、非対称を設定可能(ただし、簡易対称モードでは全ての面が対称化される)$
ii)オリジナル面と対称面をつなぎたい場合、必ず対称面上に頂点が必要$
$
上記2の制限のため、対象の中心にくる頂点を対称面(XZ平面)上に移動しておきます。$
$
1. 中心頂点を選択しやすいよいうに、表示を３面図のYZ平面にします。$
範囲選択(全面選択)で中心部分を選択します。$
$
@#tut_mirrorcenter.gif@
$
2. 表示をパースビューに戻し、作業面をXZ平面に設定します。$
メニューから[Modeling]->[選択頂点を作業面に移動]を実行すると、頂点が対称面上に移動します。$
$
@#tut_center_xz.gif@
$

3. 対称化を行う準備が出来ました。$
%AllMirror%を実行すると選択面が対称化されます。$
(対称化を解除したい場合は再度%AllMirror%を実行します)$

$
@#tut_mirror.gif@
$
(同時に曲面化も行っています)$
$
